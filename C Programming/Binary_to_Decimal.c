#include <stdio.h>
#include<math.h>

int main()
{
    int j=10101, p=1,dec=0,d=0,i=1;
    for(j;j>0;j=j/10)
    {
        d = j%10;
        if(i==1)
        {
            p=p*1;
        }
        else{
            p=p*2;
        }
        dec =dec + (d*p);
        i++;
    }
    printf("<______> %d <_____>",dec);
    return 0;
}
